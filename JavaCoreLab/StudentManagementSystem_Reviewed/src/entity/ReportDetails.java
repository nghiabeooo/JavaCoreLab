package entity;

/**
 *
 * @author Greychan
 */
public class ReportDetails {
    private String name;
    private String course;
    private int count;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    @Override
    public String toString() {
        return String.format("%-20s%-15s%-15s\n", this.name, this.course, this.count);
    }
}
